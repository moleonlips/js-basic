var a = true
var b = false

console.log('>>> a: ', a);
console.log('>>> b: ', b);

console.log('>>> a && b: ', a && b); // a and b must be True
console.log('>>> a || b: ', a || b); // atless a value be True
console.log('>>> a !== b: ', a !== b); // comparating result is v