// cau lenh re nhanh if-else

var day = Math.ceil(Math.random() * 7)

if (day === 1) {
  console.log(`sunday the king plays`);
} else if (day === 2) {
  console.log(`hello monday`);
} else if (day === 3) {
  console.log(`tuesday`);
} else if (day === 4) {
  console.log(`wednesday`);
} else if (day === 5) {
  console.log(`thur day roi`);
} else if (day === 6) {
  console.log(`thank god its friday`);
} else {
  console.log(`saturday, let's play`);
}