/**
 * ==: so sanh bang
 * >=: so sanh lon hon hoac bang
 * <=: so sanh nho hon hoac bang
 * !=: so sanh khac
 * >: so sanh lon hon
 * <: so sanh be hon
 */

var a = 10
var b = 4

if (a > b) {
  console.log(`${a} > ${b} la dung`);
} else {
  console.log(`${a} > ${b} la sai`);
}

if (a != b) {
  console.log(`${a} != ${b} la dung`);
} else {
  console.log(`${a} != ${b} la sai`);
}


if (a == b) {
  console.log(`${a} == ${b} la dung`);
} else {
  console.log(`${a} == ${b} la sai`);
}


if (a <= b) {
  console.log(`${a} <= ${b} la dung`);
} else {
  console.log(`${a} <= ${b} la sai`);
}


if (a >= b) {
  console.log(`${a} >= ${b} la dung`);
} else {
  console.log(`${a} >= ${b} la sai`);
}


if (a < b) {
  console.log(`${a} < ${b} la dung`);
} else {
  console.log(`${a} < ${b} la sai`);
}